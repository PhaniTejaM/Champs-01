package src.LoopingStatements;

public class DoWhileloop {
    public static void main(String args[]) 
{ 
    int i = 20; 
    do 
    { 
    System.out.println(i); 
    i = i+1; 
    } while (i <= 20); 


    int j=1;  
    do
    {  
    System.out.println(j);  
    j++;  
    }while(j<=10);  

} 
    
}